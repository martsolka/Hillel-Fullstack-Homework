// Оголошення змінних з різними типами даних та присвоєння їм значень:
let numberVar1 = 21;
let numberVar2 = 3.14;
let booleanVar1 = true;
let booleanVar2 = false;
let stringVar1 = "Hello, World!";
let stringVar2 = 'JavaScript';
let nullVar = null;

// Виведення типу кожної змінної в консоль:
console.log("Type of numberVar1: ", typeof numberVar1);
console.log("Type of numberVar2: ", typeof numberVar2);
console.log("Type of booleanVar1: ", typeof booleanVar1);
console.log("Type of booleanVar2: ", typeof booleanVar2);
console.log("Type of stringVar1: ", typeof stringVar1);
console.log("Type of stringVar2: ", typeof stringVar2);
console.log("Type of nullVar: ", typeof nullVar);